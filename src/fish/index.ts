// Create FISH lookup object for quiz engine
import * as fishExports from '.';

export const FISH: Record<string, any> = Object.keys(fishExports)
  .filter(key => key !== 'FISH')
  .reduce((acc, key) => {
    const fish = (fishExports as any)[key];
    if (fish && fish.id) {
      acc[fish.id] = fish;
    }
    return acc;
  }, {} as Record<string, any>);
